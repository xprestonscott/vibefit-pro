export function searchFoods(query) {
  if (!query || query.length < 1) return []
  const q = query.toLowerCase().trim()
  const words = q.split(' ')

  const results = FOOD_DATABASE.filter(f => {
    const name = f.name.toLowerCase()
    const cat  = f.category.toLowerCase()
    // Match if all words found in name or category
    return words.every(word => name.includes(word) || cat.includes(word))
  })

  // Sort by relevance - exact matches first
  results.sort((a, b) => {
    const aName = a.name.toLowerCase()
    const bName = b.name.toLowerCase()
    const aExact = aName.startsWith(q) ? 0 : aName.includes(q) ? 1 : 2
    const bExact = bName.startsWith(q) ? 0 : bName.includes(q) ? 1 : 2
    return aExact - bExact
  })

  return results.slice(0, 50)
}

export function getFoodsByCategory(category) {
  return FOOD_DATABASE.filter(f => f.category === category)
}

export const CATEGORIES = [...new Set(FOOD_DATABASE.map(f => f.category))]
